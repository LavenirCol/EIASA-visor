function getmimetype( filename ){
    var entry = filename.slice((filename.lastIndexOf(".") - 1 >>> 0) + 2);    
    var data_filter = mimetypes.filter( element => element.extension === entry);       
    var formato = "Archivo " + entry;
    if(data_filter.length > 0){
        formato = data_filter[0].formato;
    }
    return formato;
}